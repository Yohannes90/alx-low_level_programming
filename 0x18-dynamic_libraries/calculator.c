/**
 * add - calculates sum
 * @op1: operand 1
 * @op2: operand 2
 *
 * Return: sum
*/
int add(int op1, int op2)
{
	return (op1 + op2);
}

/**
 * sub - calculates difference
 * @op1: operand 1
 * @op2: operand 2
 *
 * Return: difference
*/
int sub(int op1, int op2)
{
	return (op1 - op2);
}

/**
 * mul - calculates product
 * @op1: operand 1
 * @op2: operand 2
 *
 * Return: product
*/
int mul(int op1, int op2)
{
	return (op1 * op2);
}

/**
 * div - calculates quotient
 * @op1: operand 1
 * @op2: operand 2
 *
 * Return: quotient
*/
int div(int op1, int op2)
{
	return (op1 / op2);
}

/**
 * mod - calculates remainder
 * @op1: operand 1
 * @op2: operand 2
 *
 * Return: remainder
*/
int mod(int op1, int op2)
{
	return (op1 % op2);
}
